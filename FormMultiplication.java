    private void tinhNhan() {
    try {
        double a = Double.parseDouble(txtSo1.getText());
        double b = Double.parseDouble(txtSo2.getText());

        txtKetQua.setText(String.valueOf(a * b));
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Nhập số hợp lệ!");
    }
}
