    add(lblKQ);

    txtKetQua = new javax.swing.JTextField();
    txtKetQua.setBounds(100, 190, 190, 25);
    txtKetQua.setEditable(false);
    add(txtKetQua);

    // Sự kiện nút
    btnTinh.addActionListener(e -> tinhNhan());
